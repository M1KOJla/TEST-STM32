void led_init(void) 
{
  led_periph_init();
}

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------

void led_periph_init (void)
{
  LED_PORT_init();
  LED_TIM_init ();
}

//-----------------------------------------------------------------------------
//
//-----------------------------------------------------------------------------

void LED_blink_irq (void)
{
      if (read_LED_state ())					                
      {										
        LED_off ();					
      }
      else
      {																
        LED_on ();					
      }
}

//-----------------------------------------------------------------------------
//
//-----------------------------------------------------------------------------
 
extern uint8_t button_state;

//-----------------------------------------------------------------------------
//
//-----------------------------------------------------------------------------

void LED_blink_on_off (uint8_t button_state)
{
    if (button_state)
    {
      blinc_start ();
    }
    else
    {
      blinc_stop ();
      LED_off ();
    }
}

